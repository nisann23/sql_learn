CREATE DATABASE IF NOT EXISTS movie2;
USE movie2;
-- Drop tables if they exist
DROP TABLE IF EXISTS Cast;
DROP TABLE IF EXISTS Movie;
DROP TABLE IF EXISTS Actor;

-- Create Movie table
CREATE TABLE Movie (
    id INT PRIMARY KEY,
    title VARCHAR(255),
    year INT,
    score INT,
    votes INT
);

-- Create Actor table
CREATE TABLE Actor (
    id INT PRIMARY KEY,
    name VARCHAR(255)
);

-- Create Cast table
CREATE TABLE Cast (
    movieid INT,
    actorid INT,
    ord INT,
    PRIMARY KEY (movieid, actorid),
    FOREIGN KEY (movieid) REFERENCES Movie(id),
    FOREIGN KEY (actorid) REFERENCES Actor(id)
);

